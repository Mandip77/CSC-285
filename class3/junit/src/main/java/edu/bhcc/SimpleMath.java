package edu.bhcc;

/**
 * Rectangle Class.
 *
 */
public class SimpleMath {

    /**
     * Add Two Integers.
     */
    static public int add(int a, int b) {
        return a + b;
    }

    /**
     * Multiple Two Integers.
     */
    static public int multiply(int a, int b) {
        return a * b;
    }
}
